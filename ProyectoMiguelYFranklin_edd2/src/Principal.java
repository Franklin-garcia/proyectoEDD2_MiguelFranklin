    void GuardarRegistros(String Path) {
        FileOutputStream fw = null;
        ObjectOutputStream bw = null;
        try {
            fw = new FileOutputStream(Path + "/adm_archivo.adm");
            bw = new ObjectOutputStream(fw);
            bw.writeObject(AdminRegistros);
            bw.flush();
            bw.close();
            fw.close();
        } catch (Exception e) {

        } finally {
            try {
                bw.close();
                fw.close();
            } catch (Exception e) {
            }
        }
    }

