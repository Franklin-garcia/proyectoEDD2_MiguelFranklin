    private void jButton20MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton20MouseClicked
        int llave = Integer.parseInt(JOptionPane.showInputDialog("Ingrese la llave del Registro a Eliminar"));
        Node EliminarRegistro = AdminRegistros.getBtree().search(llave);
        if (EliminarRegistro != null) {
            int pos = 0;
            System.out.println(EliminarRegistro.getKeys().toString());
            System.out.println(EliminarRegistro.getPointers().toString());
            String llaveString = "";
            llaveString = llave + "";
            for (Object key : EliminarRegistro.getKeys()) {
                if (llaveString.equalsIgnoreCase(key.toString())) {
                    break;
                }
                pos++;
            }
            long position = (long) EliminarRegistro.getPointers().get(pos);
            System.out.println("Poition long :" + position);
            try {
                AdminRegistros.deleteRegistro(position, NameFileTxt);
            } catch (IOException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
            System.out.println(AdminRegistros.PosicionRegistro(position) + "   puta " + position);
            AdminRegistros.deleteRecordTREE(llave);
            System.out.println("Arbol Eliminar");
            System.out.println(AdminRegistros.getBtree().print());
            //lbl_borrar_registro.setText("Registro Borrado Exitosamente!");
            JOptionPane.showMessageDialog(this, "Se elimino correctamente");
        } else {
            //lbl_borrar_registro.setText("Registro no Existe!");
            JOptionPane.showMessageDialog(this, "El Registro no existe");
        }


    }//GEN-LAST:event_jButton20MouseClicked

